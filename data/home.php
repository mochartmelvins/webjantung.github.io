					<p class="justify">Heart disease is a condition when the heart is disturbed. The form of the disorder itself can vary. There are disorders of the heart's blood vessels, heart rhythm, heart valves, or congenital disorders.</p>
                    <br><p>The heart is a muscle that is divided into four chambers. Two chambers are located at the top, namely the right and left atrium. While the two more chambers are located at the bottom, namely the right and left ventricles. Between the right and left spaces are separated by a muscular wall (septum) which functions to prevent the mixing of oxygen-rich blood with oxygen-poor blood.</p>
                    <h3 class="subtitle">Types of Heart Disease</h3>
					<ul class="checkmark pad-left">
						<li>Coronary artery disease (coronary heart disease) – narrowing of the arteries of the heart.</li>
						<li>Arrhythmias – disturbances in the heart rhythm.</li>
						<li>Congenital heart disease – heart defects from birth.</li>
                        <li>Cardiomyopathy – disorders of the heart muscle.</li>
					</ul>
                    
