                    <h3 class="subtitle">Some of the symptoms of heart disease include:</h3>
					<ul class="checkmark pad-left">
						<li>Coronary heart disease, the main symptom is chest pain, which can be accompanied by shortness of breath, pain that radiates to the left arm, or to the jaw.</li>
						<li>Congenital heart disease, symptoms can include shortness of breath and look blue when crying or breastfeeding, swelling around the eyes and in the legs, and not gaining weight.</li>
						<li>Heart infection (endocarditis), symptoms include fever, irregular heartbeat, shortness of breath, swelling in the abdomen or legs, and coughing.</li>
                        <li>Heart failure, the symptoms are shortness of breath that gets worse when doing activities, shortness of breath that gets worse when lying down, swollen legs, and enlarged abdomen.</li>
					</ul>
                    
